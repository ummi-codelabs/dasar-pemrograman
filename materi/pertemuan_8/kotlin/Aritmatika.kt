class Aritmatika {

    fun penjumlahan(a: Int, b: Int): Double {
        return (a + b).toDouble()
    }

    fun pengurangan(a: Int, b: Int): Double {
        return (a - b).toDouble()
    }

    fun perkalian(a: Int, b: Int): Double {
        return (a * b).toDouble()
    }

    fun pembagian(a: Int, b: Int): Double {
        return (a / b).toDouble()
    }

    fun hasilBagi(a: Int, b: Int): Double {
        return (a % b).toDouble()
    }

}